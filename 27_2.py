# Условия те же. Но теперь нужно найти наибольшее число R, удовлетворяющее следующим условиям:
#   1. R — произведение двух различных переданных элементов последовательности («различные» означает, что не рассматриваются квадраты переданных чисел, произведения различных элементов последовательности, равных по величине, допускаются);
#   2. R делится на 15.

""" Произведение делится на 15:
        1. одно на 15, второе любое
        2. одно на 5, другое на 3
"""
# Подготовка данных
o = open('27.txt')
n = int(o.readline())
M = 0      # максимум
M3 = 0     # максимум кратный 3
M5 = 0     # максимум кратный 5
M15 = 0    # максимум кратный 15
for i in range(n):
    a = int(o.readline())
    if a % 15 == 0 and a > M15:      # если а кратно 15 и больше М15
        M = max(M15, M) # Перезаписывае М. Так как в М15 может быть число больше
        M15 = a # значит а максимум %15
    else:
        M = max(M, a)
        if a % 3 == 0 and a > M3:
            M3 = a
        elif a % 5 == 0 and a > M5:
            M5 = a
print(max(M15*M, M3*M5))
