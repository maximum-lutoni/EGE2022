# По каналу связи передавалась последовательность положительных целых чисел, все числа не превышают 1000. 
# Количество чисел известно. Затем передаётся контрольное значение последовательности — наименьшее число R,
#  удовлетворяющее следующим условиям:
#   1. R — произведение двух различных переданных элементов последовательности («различные» означает, что не рассматриваются квадраты переданных чисел, произведения различных элементов последовательности, равных по величине, допускаются);
#   2. R делится на 3.
# Гарантируется, что такое число существует.
# Программа должна напечатать одно число — вычисленное контрольное значение, соответствующую условиям задачи.

# Входные данные. 
# Даны два входных файла (файл A и файл B), каждый из которых содержит в первой строке количество пар N (1 ≤ N ≤ 100000).
# Каждая из следующих N строк содержит одно натуральное число, не превышающее 1000. 

# Пример организации исходных данных во входном файле: 
# 7
# 12
# 3
# 5
# 20
# 33
# 13
# 123
# Для указанных входных данных значением искомого произведения должно быть число 15.

""" Для этой и для селедующих задач:
   Произведение чисел для двух чисел a,b картно 6 в 2 случаях.
   a%6 == 0, b -любое
   a%3 == 0, b % 2 == 0

   В нашей задаче 3 - простое. Значит произведение кратно 3, если одно число кратно 3.
   Итого: минимально произведение кратное 3 = минимальное число кратное 3 * минимальное число
"""

o = open('27.txt')
n = int(o.readline())
m = 1001    # минимум чисел
m3 = 1001   # минимум кратный 3
for i in range(n):
    a = int(o.readline())     # считываем а
    if a % 3 == 0 and a < m3: # проверяем а на кратность и минимум
        m = min(m3, m) 
        m3 = a
    else:
        m = min(m, a)  # если а не кратно проверяем его на минимум
print(m*m3)

