# На вход программы поступает последовательность из N целых положительных чисел, все числа в последовательности различны. 
# Рассматриваются все пары различных элементов последовательности (элементы пары не обязаны стоять в последовательности рядом,
# порядок элементов в паре не важен). Необходимо определить количество пар, для которых произведение элементов кратно 33.

o = open('27.txt')
N = int(o.readline())
k33 = 0     # кратные 33
k11 = 0     # кратные 11
k3 = 0      # кратные 3   
for i in range(N):
    a = int(o.readline())
    if a % 33 == 0:
        k33 += 1
    elif a % 11 == 0: 
        k11 += 1
    elif a % 3 == 0:   
        k3 += 1
print(k11 * k3 + k33 * (N - k33) + k33 * (k33 - 1)/2) 







